for numero in range (1, int(input('Digite um número para determinar o fim: ')), 1):
    print('\t' + str(numero))

# Range especifica uma faixa de valores e como será incrementada.
# A variável iniciará com o valor 1 até o valor que for digitado pelo usuário.
# A faixa será incrementada de 1 em 1, conforme o último valor especificado no range